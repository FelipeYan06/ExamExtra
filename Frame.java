    public void setModelo(){
        String[] tlbCabecera = {"No ","ID","Nombre","UA","Milesk"};
        dataPlanetas.setColumnIdentifiers(tlbCabecera);
        tlbPlanetas.setModel(dataPlanetas);
    }
    
    public void setDatos(){
        Object[] datos = new Object[dataPlanetas.getColumnCount()];
        int i =0;
        dataPlanetas.setRowCount(0);
        for (Planeta planeta : planetas) {
            datos[0] = i;
            datos[1] = planeta.getiD();
            datos[2] = planeta.getNombre();
            datos[3] = planeta.getUA();
            datos[4] = planeta.getMilesk();

            i++;
            dataPlanetas.addRow(datos);
        }
        tlbPlanetas.setModel(dataPlanetas);
        
    }
    
    private void btnAñadirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAñadirMouseClicked
       
        int recNo = Integer.parseInt(this.txtRecNo.getText().trim());
        String iD = this.txtId.getText();
        String nombre = this.txtNombre.getText();
        String UA = this.txtUA.getText();
        String milesk = this.txtMilesk.getText();
        
              
                
        /*
        for (Planeta planeta : planetas) {
          txtAreaMostrarEvidencia.setText(txtAreaMostrarEvidencia.getText()+"\n"+planeta);
        }
       */
        
        if (recNo == -1) {
            Planeta.añadirPlanetas(UA, iD,  milesk,  nombre);
        }else{
            Planeta.actualizarPlaneta(recNo, iD, nombre, UA,milesk );
            this.tgBtnActualizar.setSelected(false);
        }

        setDatos();
        limpiarCampos(); 
        
    }//GEN-LAST:event_btnAñadirMouseClicked

