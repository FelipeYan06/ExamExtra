    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        int filaActual = tlbPlanetas.getSelectedRow();

        if (filaActual != -1) {
            Planeta.eliminarPlanetas(filaActual);
            setDatos();
        } else{
            JOptionPane.showMessageDialog( null, "No hay filas existentes para eliminar");
        }
     }//GEN-LAST:event_btnEliminarMouseClicked

    private void tgBtnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tgBtnActualizarMouseClicked

        int filaActual = tlbPlanetas.getSelectedRow();
        System.out.println(filaActual);
        System.out.println(this.tgBtnActualizar.isSelected());
        
        if (this.tgBtnActualizar.isSelected()) {
            if (filaActual != -1) {
                
                this.txtRecNo.setText(""+dataPlanetas.getValueAt(filaActual, 0));
                this.txtId.setText(""+dataPlanetas.getValueAt(filaActual, 1));
                this.txtNombre.setText(""+dataPlanetas.getValueAt(filaActual, 2));
                this.txtUA.setText(""+dataPlanetas.getValueAt(filaActual, 3));
                this.txtMilesk.setText(""+dataPlanetas.getValueAt(filaActual, 4));

            }else{
                JOptionPane.showMessageDialog( null, "Debe seleccionar un registro");
                this.tgBtnActualizar.setSelected(false);
            } 
        }else{
            limpiarCampos();
        }
    }//GEN-LAST:event_tgBtnActualizarMouseClicked

